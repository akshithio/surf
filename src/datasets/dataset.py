"""PyTorch dataset wrapper around SURF multimodal Zarr stores."""

from dataclasses import dataclass
from datetime import datetime
from pathlib import Path

import numpy as np
import torch
import zarr
from torch.utils.data import Dataset


@dataclass
class ZarrShapes:
    num_patches: int
    timesteps: int
    s2_channels: int
    s1_channels: int
    climate_channels: int
    patch_h: int
    patch_w: int


def _decode_bytes_1d(arr: np.ndarray) -> list[str]:
    out = []
    for x in arr:
        if isinstance(x, bytes):
            out.append(x.decode("utf-8").rstrip("\x00"))
        else:
            out.append(str(x))
    return out


class MultimodalPatchDataset(Dataset):
    """
    Dataset over patch-indexed multimodal time series generated by the patch writer.

    Each sample returns:
      - s2: (T, C2, H, W)
      - s1: (T, C1, H, W)
      - climate: (T, Cc, H, W)
      - s2_mask/s1_mask/climate_mask: (T, H, W)
      - doy: (T,) in [1, 366]
      - patch_xy: (2,)
    """

    def __init__(
        self,
        zarr_path: str | Path,
        device: str = "cpu",
        s2_reflectance_divisor: float | None = None,
    ) -> None:
        self.zarr_path = Path(zarr_path).expanduser().resolve()
        self.root = zarr.open_group(str(self.zarr_path), mode="r")
        self.s2 = self.root["s2"]
        self.s1 = self.root["s1"]
        self.s2_mask = self.root["s2_mask"]
        self.s1_mask = self.root["s1_mask"]
        self.climate = self.root["climate"] if "climate" in self.root else None
        self.climate_mask = self.root["climate_mask"] if "climate_mask" in self.root else None
        self.patch_xy = self.root["patch_xy"] if "patch_xy" in self.root else None
        if s2_reflectance_divisor is not None and s2_reflectance_divisor <= 0:
            raise ValueError("s2_reflectance_divisor must be positive")
        self.s2_reflectance_divisor = s2_reflectance_divisor
        self.s2_reflectance_indices: list[int] = []
        if s2_reflectance_divisor is not None:
            if "s2_bands" not in self.root:
                raise KeyError("Expected `s2_bands` when applying S2 reflectance normalization.")
            self.s2_reflectance_indices = [
                index
                for index, band in enumerate(_decode_bytes_1d(np.asarray(self.root["s2_bands"][:])))
                if band != "NDVI"
            ]

        self.s2_time_ns = self.root["s2_time_ns"] if "s2_time_ns" in self.root else None
        self.s1_time_ns = self.root["s1_time_ns"] if "s1_time_ns" in self.root else None
        if self.s2_time_ns is None:
            if "time" not in self.root:
                raise KeyError("Expected `time` or `s2_time_ns` array in zarr store.")
            self.time_strings = _decode_bytes_1d(np.asarray(self.root["time"][:]))
            self.doy = self._compute_doy(self.time_strings)
        else:
            self.time_strings = []
            self.doy = None
        self.device = torch.device(device)

        s2_shape = self.s2.shape
        self.shapes = ZarrShapes(
            num_patches=int(s2_shape[0]),
            timesteps=int(s2_shape[1]),
            s2_channels=int(s2_shape[2]),
            s1_channels=int(self.s1.shape[2]),
            climate_channels=int(self.climate.shape[2]) if self.climate is not None else 0,
            patch_h=int(s2_shape[3]),
            patch_w=int(s2_shape[4]),
        )

    @staticmethod
    def _compute_doy(time_strings: list[str]) -> torch.Tensor:
        values = [datetime.strptime(ts, "%Y-%m-%d").timetuple().tm_yday for ts in time_strings]
        return torch.tensor(values, dtype=torch.float32)

    @staticmethod
    def _time_features(time_ns: np.ndarray) -> tuple[torch.Tensor, torch.Tensor]:
        timestamps = np.asarray(time_ns, dtype="datetime64[ns]")
        days = timestamps.astype("datetime64[D]")
        year_start = timestamps.astype("datetime64[Y]").astype("datetime64[D]")
        doy = (days - year_start).astype(np.int64) + 1
        elapsed = (days - days[0]).astype(np.int64)
        return torch.from_numpy(doy.astype(np.float32)), torch.from_numpy(elapsed.astype(np.float32))

    def __len__(self) -> int:
        return self.shapes.num_patches

    def __getitem__(self, idx: int) -> dict[str, torch.Tensor]:
        s2 = torch.from_numpy(np.asarray(self.s2[idx], dtype=np.float32))
        if self.s2_reflectance_divisor is not None:
            s2[:, self.s2_reflectance_indices] /= self.s2_reflectance_divisor
        s1 = torch.from_numpy(np.asarray(self.s1[idx], dtype=np.float32))
        s2_mask = torch.from_numpy(np.asarray(self.s2_mask[idx], dtype=np.float32))
        s1_mask = torch.from_numpy(np.asarray(self.s1_mask[idx], dtype=np.float32))
        if self.climate is None:
            climate = torch.empty((self.shapes.timesteps, 0, self.shapes.patch_h, self.shapes.patch_w))
            climate_mask = torch.zeros((self.shapes.timesteps, self.shapes.patch_h, self.shapes.patch_w))
        else:
            climate = torch.from_numpy(np.asarray(self.climate[idx], dtype=np.float32))
            climate_mask = torch.from_numpy(np.asarray(self.climate_mask[idx], dtype=np.float32))
        if self.patch_xy is None:
            patch_xy = torch.zeros(2, dtype=torch.int64)
        else:
            patch_xy = torch.from_numpy(np.asarray(self.patch_xy[idx], dtype=np.int64))

        if self.s2_time_ns is None:
            doy = self.doy.clone()
            elapsed_days = doy - doy[0]
            s2_doy = doy.clone()
            s1_doy = doy.clone()
            s2_elapsed_days = elapsed_days.clone()
            s1_elapsed_days = elapsed_days.clone()
        else:
            s2_doy, s2_elapsed_days = self._time_features(np.asarray(self.s2_time_ns[idx], dtype=np.int64))
            if self.s1_time_ns is None:
                s1_doy, s1_elapsed_days = s2_doy.clone(), s2_elapsed_days.clone()
            else:
                s1_doy, s1_elapsed_days = self._time_features(np.asarray(self.s1_time_ns[idx], dtype=np.int64))
            doy = s2_doy

        # Time-step level availability (1 if any pixel in the patch is present).
        s2_available = (s2_mask.flatten(start_dim=1).sum(dim=1) > 0).float()
        s1_available = (s1_mask.flatten(start_dim=1).sum(dim=1) > 0).float()
        climate_available = (climate_mask.flatten(start_dim=1).sum(dim=1) > 0).float()

        sample = {
            "s2": s2,
            "s1": s1,
            "climate": climate,
            "s2_mask": s2_mask,
            "s1_mask": s1_mask,
            "climate_mask": climate_mask,
            "s2_available": s2_available,
            "s1_available": s1_available,
            "climate_available": climate_available,
            "doy": doy,
            "s2_doy": s2_doy,
            "s1_doy": s1_doy,
            "s2_elapsed_days": s2_elapsed_days,
            "s1_elapsed_days": s1_elapsed_days,
            "patch_xy": patch_xy,
        }
        if "elevation" in self.root:
            sample["elevation"] = torch.tensor(float(self.root["elevation"][idx]), dtype=torch.float32)
        if self.device.type != "cpu":
            return {k: v.to(self.device, non_blocking=True) for k, v in sample.items()}
        return sample